<h2>¿Cómo funciona?</h2>
La persona escucha la música o la voz con auriculares especiales que transmiten el sonido por vía aérea (a través de  los oídos) y por vía  ósea (a través de los huesos). El sonido actúa sobre los músculos del oído medio, estimula la cóclea y el sistema vestibular en el oído interno preparando la recepción adecuada de las frecuencias. Lo anterior permite al cerebro un mejor procesamiento de la información propia y del medio.

<span>Las frecuencias de sonido potencializan diferentes aspectos en el ser humano:</span>

<hr>

</div>
<div class="infoQue-wraper infoQue">
<div class="infoQue--item">
<div class="infoQue--caption">
<h2>Los sonidos de las frecuencias bajas o graves:</h2>
Regulan nuestra función vestibular, el desarrollo sensorial y motor; la coordinación, la postura, el equilibrio, el movimiento, el ritmo, la organización, la planeación de la actividad en función del tiempo y el espacio; y brindan mayor auto-control de los impulsos.

</div>
</div>
<div class="infoQue--item block1">
<div class="infoQue--caption ">
<h2>Los sonidos de las frecuencias medias:</h2>
Estimulan la comunicación, el lenguaje y el aprendizaje. Favorecen las relaciones interpersonales y de grupo.

</div>
</div>
<div class="infoQue--item block1">
<div class="infoQue--caption">
<h2>Los sonidos de las frecuencias altas o agudas:</h2>
favorecen la actividad intelectual, la creatividad y el bienestar emocional. Facilitan una mejor comprensión del otro y el sentimiento de empatía.

