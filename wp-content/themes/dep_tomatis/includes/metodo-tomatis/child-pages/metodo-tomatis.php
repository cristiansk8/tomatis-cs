<h2>¿Por qué se llama Método Tomatis?</h2>
El nombre proviene de su creador el Otorrinolaringólogo e Investigador francés Alfred A. Tomatis (1920-2001). Sus investigaciones revolucionaron la comprensión del oído y su relación con los trastornos sensorio-motores, emocionales, cognitivos y comunicativos.

Todas las relaciones que descubrió entre otorrinolaringología y psicología dieron lugar a una nueva disciplina <i>la audiopsicofonología.</i> El Dr. Tomatis dedicó toda su vida a mejorar su Método y creó la organización que forma profesionales altamente cualificados <b>Tomatis Developpement</b>

Su trabajo con cantantes y trabajadores de aviación que sufrían sordera profesional, le permitió identificar la diferencia fundamental entre oír y escuchar. Oír solo incluye la recepción pasiva del sonido. Escuchar implica seleccionar la información sensorial para darle sentido.

