    <h2>¿Por qúe es importante escuchar bien?</h2>
    El ser humano es un ser comunicativo por excelencia, se comunica consigo mismo y con su entorno. <b>La forma como escucha es la forma como se relaciona.</b> Escuchar es diferente de oír. Oír es pasivo, oímos incluso estando dormidos, escuchar en cambio, es activo. Escuchar  implica movilizar nuestra atención e intención para centrarnos en un estímulo descartando todos los que son irrelevantes.

    <b>Escuchar es la capacidad de utilizar el oído de manera intencional y atenta con el fin de comprender,  comunicar y aprender, sin que ello resulte perturbador a nivel emocional.</b>

    <hr>

    <span>El oído es más que un instrumento para oír</span>

