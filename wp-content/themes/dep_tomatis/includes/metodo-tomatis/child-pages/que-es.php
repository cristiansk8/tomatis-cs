<h2>¿Qué es?</h2>
Es una <b>estimulación auditiva neurosensorial.</b> Utiliza música con contrastes imprevisibles de timbre e intensidad que sorprenden al cerebro y hacen que tu capacidad de escuchar mejore. Cuando escuchas bien, todas tus habilidades comunicativas, cognitivas, sensoriales, motoras, sociales y emocionales se favorecen.

<span>El Método Tomatis utiliza <b>diferentes composiciones musicales</b>, cada una con un objetivo distinto:</span>

