<h2>Voz de la mama:</h2>
El Dr. Alfred Tomatis fue pionero en afirmar que el bebé en el útero escucha desde el cuarto mes de gestación, generando memorias embrionarias y fetales en el aparato auditivo. Al recuperar estas memorias a través de la voz materna el niño obtiene mayor seguridad, recupera el deseo vital de comunicarse y experimenta una relación más armónica con el entorno.
