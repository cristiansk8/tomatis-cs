<h2>Música de Mozart:</h2>
Mejora la memoria, estimula la creatividad y facilita el pensamiento lógico. Favorece el desarrollo motor, aumenta la capacidad de aprendizaje y brinda mayor eficiencia en las actividades estudiantiles y laborales.

“La música de Mozart a través del equipo TalksUp se convierte en un armonizador. La persona manifiesta un deseo de comunicarse con su entorno y su postura se transforma para escuchar el mundo y su interior” Dr. Alfred Tomatis.

