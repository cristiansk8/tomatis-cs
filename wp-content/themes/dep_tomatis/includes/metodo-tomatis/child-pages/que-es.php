<h2>Cantos Gregorianos:</h2>
Modifican las referencias neurovegetativas. La respiración se siente más amplia y el pulso se regula generando una sensación de calma. Brinda auto confianza e invita a la verticalidad, es decir, a poner la cabeza en la posición correcta, para hacerse visible y mostrarse al mundo.

